fn solve(k: i64, rest: i64) -> i64 {
    if rest == 1 {
        k
    } else {
        (1..=k).map(|i| solve(k / i, rest - 1)).sum::<i64>()
    }
}

fn main() {
    input! {
        k: i64
    };

    println!("{}", solve(k, 3));
}
